public class Kpi{
    public double x;
    public String y;

    public Kpi(double x, String y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public String getY() {
        return y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(String y) {
        this.y = y;
    }
}